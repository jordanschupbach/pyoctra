/* Compute the greatest common divisor of positive integers */
int gcd(int x, int y) {
  int g;
  g = y;
  while (x > 0) {
    g = x;
    x = y % x;
    y = g;
  }
  return g;
}

Circle::Circle(double radius) : m_radius(radius) {}

double Circle::area() {
  return 3.14159 * m_radius * m_radius;
}

double Circle::perimeter() {
  return 2 * 3.14159 * m_radius;
}
